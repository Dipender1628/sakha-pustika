// Function to format content with bold headings
function formatContent(content: string) {
  // Split by lines and process each line
  const lines = content.split('\n')
  const formatted: React.ReactElement[] = []
  
  lines.forEach((line, index) => {
    // Check if line starts with a number followed by a dot (like "1. ", "2. ", etc.)
    // Match headings that end with regular dash "–" (for charcha 3 format)
    const headingMatch = line.match(/^(\d+\.\s+[^–]+–)/)
    
    // Check if line is "निष्कर्ष" or "समाधान (Solutions)" or similar section headings
    const sectionHeadingMatch = line.match(/^(निष्कर्ष|समाधान\s*\(Solutions\)|भारत को सुपरपावर बनाने का रोडमैप)/)
    
    // Check for standalone numbered headings (charcha 1 and 2 format)
    // This includes headings with or without em dash "—"
    const standaloneHeadingMatch = line.match(/^(\d+\.\s+.+)$/)
    
    if (headingMatch) {
      // This is a numbered heading with regular dash (charcha 3 format) - extract heading and rest of text
      const heading = headingMatch[1].trim()
      const restOfLine = line.substring(heading.length).trim()
      
      formatted.push(
        <p key={index} className="text-gray-800 mb-2">
          <span className="font-bold text-gray-900">{heading}</span>
          {restOfLine && ` ${restOfLine}`}
        </p>
      )
    } else if (standaloneHeadingMatch && !line.match(/^(\d+\.\s+[^–]+–)/)) {
      // This is a standalone numbered heading (charcha 1 and 2 format) - make it bold
      // But exclude if it matches the charcha 3 format (with regular dash)
      formatted.push(
        <p key={index} className="font-bold text-gray-900 mt-4 mb-2">
          {line}
        </p>
      )
    } else if (sectionHeadingMatch) {
      // This is a section heading - make it bold
      formatted.push(
        <p key={index} className="font-bold text-gray-900 mt-4 mb-2">
          {line}
        </p>
      )
    } else if (line.trim() === '') {
      // Empty line - add spacing
      formatted.push(<br key={index} />)
    } else {
      // Regular text
      formatted.push(
        <p key={index} className="text-gray-800 mb-2">
          {line}
        </p>
      )
    }
  })
  
  return <div>{formatted}</div>
}

